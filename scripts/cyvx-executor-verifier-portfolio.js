#!/usr/bin/env node
"use strict";

const fs = require("fs");
const cp = require("child_process");
const path = require("path");

function read(p){ try { return fs.readFileSync(p,"utf8"); } catch { return ""; } }
function json(p){ try { return JSON.parse(read(p)); } catch { return null; } }
function write(p,v){ fs.mkdirSync(path.dirname(p),{recursive:true}); fs.writeFileSync(p, typeof v==="string" ? v : JSON.stringify(v,null,2)); }
function sh(cmd){
  try { return { ok:true, out:cp.execSync(cmd,{encoding:"utf8",stdio:["ignore","pipe","pipe"],maxBuffer:1024*1024*30}) }; }
  catch(e){ return { ok:false, out:String(e.stdout || e.stderr || e.message || e) }; }
}
function slug(s){ return String(s || "capability").toLowerCase().replace(/[^a-z0-9]+/g,"-").replace(/^-|-$/g,""); }

const now = new Date().toISOString();
const compiled = json("data/capability-compiler/latest-compiled-capability.json") || {};
const flywheel = json("data/intelligence/flywheel-state.json") || {};
const decision = json("data/decision-compression/executive-state.json") || {};
const live = json("data/runtime/live-dashboard-state.json") || {};

const selected = compiled.selected_capability || live.selectedCapability || "Prediction Ledger + Simulation Ranking";
const id = "exec-" + Date.now();
const capabilitySlug = slug(selected);

const execution = {
  id,
  created_at: now,
  system: "CYVX Executor + Verifier",
  selected_capability: selected,
  mode: "safe-deterministic-local",
  principle: "Generate artifacts, verify outputs, avoid uncontrolled external execution.",
  build_plan: {
    folders: [
      `core/generated-capabilities/${capabilitySlug}`,
      `data/generated-capabilities/${capabilitySlug}`,
      "docs/evidence"
    ],
    files: [
      `core/generated-capabilities/${capabilitySlug}/README.md`,
      `data/generated-capabilities/${capabilitySlug}/state.json`,
      `docs/evidence/CYVX_GENERATED_CAPABILITY_${capabilitySlug.toUpperCase().replace(/-/g,"_")}.md`
    ]
  },
  validation_plan: [
    "compiled capability exists",
    "generated core artifact exists",
    "generated data artifact exists",
    "generated evidence report exists",
    "Omega100 validator runs",
    "audit scanner runs if present"
  ],
  outcome_target: `CYVX can move from capability gap to generated artifact and verification report for ${selected}.`,
  status: "planned"
};

const generatedState = {
  capability: selected,
  source: "Capability Compiler",
  generated_at: now,
  purpose: "Materialized capability artifact generated by CYVX Executor + Verifier.",
  inputs: compiled.specification?.inputs || [],
  outputs: compiled.specification?.outputs || [],
  tasks: compiled.tasks || [],
  success_metric: compiled.specification?.success_metric || "Capability artifact exists and passes local verification.",
  next_step: "Wire this generated capability into API/UI/runtime if it proves valuable.",
  status: "generated"
};

write(`core/generated-capabilities/${capabilitySlug}/README.md`,
`# ${selected}

Generated by CYVX Executor + Verifier.

## Purpose

${generatedState.purpose}

## Success Metric

${generatedState.success_metric}

## Next Step

${generatedState.next_step}
`);

write(`data/generated-capabilities/${capabilitySlug}/state.json`, generatedState);

write(`docs/evidence/CYVX_GENERATED_CAPABILITY_${capabilitySlug.toUpperCase().replace(/-/g,"_")}.md`,
`# CYVX Generated Capability

Generated: ${now}

## Capability

${selected}

## Outcome Target

${execution.outcome_target}

## Generated Artifacts

${execution.build_plan.files.map(f=>"- "+f).join("\n")}

## Validation Plan

${execution.validation_plan.map(x=>"- "+x).join("\n")}

## Status

generated
`);

execution.status = "generated";
write("data/execution/latest-execution-plan.json", execution);

const checks = [
  ["compiled-capability", !!compiled.selected_capability || !!selected],
  ["core-artifact", fs.existsSync(`core/generated-capabilities/${capabilitySlug}/README.md`)],
  ["data-artifact", fs.existsSync(`data/generated-capabilities/${capabilitySlug}/state.json`)],
  ["evidence-artifact", fs.existsSync(`docs/evidence/CYVX_GENERATED_CAPABILITY_${capabilitySlug.toUpperCase().replace(/-/g,"_")}.md`)],
  ["omega100", sh('NODE_OPTIONS="--no-warnings" node scripts/cyvx-validation-matrix-omega100.js').ok],
  ["audit-scanner", fs.existsSync("scripts/cyvx-audit-and-fix.js") ? sh("node scripts/cyvx-audit-and-fix.js").ok : true]
];

const passed = checks.filter(x=>x[1]).length;
const verification = {
  id: "verify-" + Date.now(),
  created_at: now,
  system: "CYVX Verifier",
  execution_id: id,
  selected_capability: selected,
  passed,
  total: checks.length,
  score: Math.round((passed / checks.length) * 100),
  checks: checks.map(([name, ok]) => ({ name, status: ok ? "PASS" : "FAIL" })),
  status: passed === checks.length ? "verified" : "needs-review"
};

write("data/verification/latest-verification.json", verification);

const candidates = [
  {
    capability: selected,
    source: "latest compiler output",
    impact: 0.92,
    confidence: verification.score / 100,
    unlocks: 0.88,
    cost: 0.15,
    risk: verification.status === "verified" ? 0.1 : 0.25
  },
  {
    capability: "Prediction Ledger + Simulation Ranking",
    source: "flywheel",
    impact: 0.94,
    confidence: 0.87,
    unlocks: 0.91,
    cost: 0.18,
    risk: 0.14
  },
  {
    capability: "Continuous Reality Ingestion",
    source: "future platform",
    impact: 0.9,
    confidence: 0.82,
    unlocks: 0.86,
    cost: 0.22,
    risk: 0.18
  },
  {
    capability: "Public Proof Surface",
    source: "adoption",
    impact: 0.88,
    confidence: 0.84,
    unlocks: 0.8,
    cost: 0.18,
    risk: 0.12
  },
  {
    capability: "Outcome History + Trust Trends",
    source: "learning loop",
    impact: 0.86,
    confidence: 0.88,
    unlocks: 0.84,
    cost: 0.14,
    risk: 0.1
  }
].map(c => ({
  ...c,
  portfolio_score: Number((c.impact * c.confidence * c.unlocks - c.cost - c.risk).toFixed(3))
})).sort((a,b)=>b.portfolio_score-a.portfolio_score);

const portfolio = {
  created_at: now,
  system: "CYVX Portfolio Brain",
  question: "What single capability creates the most future capability?",
  current_platform_position: {
    foundation: "complete",
    intelligence: "functional",
    execution: "emerging",
    verification: verification.status,
    adoption: "needs real proof volume"
  },
  candidates,
  selected_next_capability: candidates[0],
  portfolio_strategy: [
    "Prefer capabilities that create more capabilities.",
    "Prefer proof-generating systems over dashboard-only systems.",
    "Prefer measurable loops over static reports.",
    "Prefer low-risk deterministic execution before autonomous external execution."
  ],
  next_best_action: `Build or deepen: ${candidates[0].capability}`,
  status: "active"
};

write("data/portfolio/portfolio-brain-state.json", portfolio);

write("core/executor-verifier/README.md",
"# CYVX Executor + Verifier\n\nCapability Gap → Build Plan → Generated Artifact → Verification → Evidence → Outcome Target\n");

write("core/portfolio-brain/README.md",
"# CYVX Portfolio Brain\n\nRanks capabilities by future capability unlocked, confidence, impact, cost, and risk.\n");

write("docs/evidence/CYVX_EXECUTOR_VERIFIER_PORTFOLIO.md",
`# CYVX Executor + Verifier + Portfolio Brain

Generated: ${now}

## Executed Capability

${selected}

## Execution Status

${execution.status}

## Verification

${verification.status} — ${verification.score}/100

${verification.checks.map(c=>`- ${c.status}: ${c.name}`).join("\n")}

## Portfolio Brain

Question: ${portfolio.question}

## Selected Next Capability

${portfolio.selected_next_capability.capability}

Score: ${portfolio.selected_next_capability.portfolio_score}

## Next Best Action

${portfolio.next_best_action}

## Capability Rankings

${portfolio.candidates.map((c,i)=>`${i+1}. ${c.capability} — score ${c.portfolio_score}`).join("\n")}
`);

write("data/runtime/live-dashboard-state.json", {
  ...live,
  health: "active",
  modelHealth: "executor-verifier-portfolio-brain",
  trust: Math.max(Number(live.trust || 91), verification.status === "verified" ? 92 : 90),
  topConstraint: "CYVX can now compile and materialize capability artifacts, but needs repeated verified executions to expand safely.",
  nextAction: portfolio.next_best_action,
  nextBestAction: {
    title: portfolio.next_best_action,
    why: "Portfolio Brain selected the capability with the highest future-capability leverage.",
    confidence: portfolio.selected_next_capability.confidence
  },
  executorVerifier: {
    status: verification.status,
    score: verification.score,
    latest_execution: id
  },
  portfolioBrain: {
    status: "active",
    selected_next_capability: portfolio.selected_next_capability.capability,
    score: portfolio.selected_next_capability.portfolio_score
  }
});

console.log("CYVX Executor + Verifier + Portfolio Brain complete.");
console.log("Executed Capability:", selected);
console.log("Verification:", verification.status, verification.score + "/100");
console.log("Portfolio Next:", portfolio.next_best_action);
